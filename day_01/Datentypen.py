"Zusatz zur Aufgabe 1: Alle Datentypen"

# INT ist für Ganze Zahlen
zahl_int = 20

# STRING ist für eine Text zeile der text muss in "" eingeschrieben werden!
text_string = "Hallo ich bin ein String"

# FLOAT ist für Gleitkommazahlen
zahl_float = 1.25

# List für Geordnete listen und Veränderbare Sammlungen von Elementen
liste = [1, 'apfel', 2, 3]

# tuple Geordnet aber unveränderbare Sammlungen von elementen
un_list = ("Affe", "Bär", "Löwe")

#range eine unverändliche Sequenz von Zahlen
reichweite = range(1, 5)

#BOOL, Wahrheitswerte entweder True oder False
tr_ue = True
fa_lse = False

#dict Ungeordnete oder Geordnete sammlungen von Schlüssel-wert-paaren 
dictionary = {'Name': 'Hans', 'Alter': 30}

#set: Eine ungeordnete Sammlung von eindeutigen Elementen
thisset = {"apple", "banana", "pineapple"}

#Jeder print befehl gibt das jeweilige Datentyp auf der Konsole aus
print(zahl_int)
print(text_string)
print(zahl_float)
print(liste)
print(un_list)
print(reichweite)
print(tr_ue)
print(fa_lse)
print(dictionary)
print(thisset)

