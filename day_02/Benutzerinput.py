"Aufgabe 2: Benutzerinputs -> Lieblingszahl + kleine Berechnung"

# Variable mit einem Benutzer input für eine ganze zahl
zahl = int(input("Wie lautet deine Lieblings zahl?: "))

# Zusätzliche INT variable zum addieren mit unserer lieblings zahl
a = 20

# Kleine rechnung als variable
addition = zahl + a

# Print befehl zum ausgeben
print("deine lieblings zahl addiert mit 20 ergibt: ", addition)

